// Incrementar o decrementar un numero

let puntaje = 5;

// -------------------PROBAR EN LA CONSOLA DEL NAVEGADOR------------------------

// Incrementar 1 a 1
puntaje++; // 5 Suma sin mostrar hasta que lo llamas nuevamente
++puntaje; // 7 Suma y muestra instantaneamente sin llamarlo

// Decrementar
puntaje--; // 5 
--puntaje; // 4 

// Incrementar o decrementar de a mas de 1 en 1
puntaje += 3;
puntaje -= 3;






console.log(puntaje);