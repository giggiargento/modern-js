const producto = {
    nombre: "Monitor 20 pulgadas",
    precio: 300,
    disponible: true,
}

// Keys retorna los "id" dentro de un objeto
console.log(Object.keys(producto));

// Values retorna los valores de un "id" dentro de un objeto 
console.log(Object.values(producto));

// Entries retorna todo ordenado
console.log(Object.entries(producto));